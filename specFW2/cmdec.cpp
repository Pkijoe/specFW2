#include "StdAfx.h"
#include "SpecFW2.h"
#include "ParserThread.h"

unsigned int CParserThread::cmdEC()
{
	theApp.EnterCriticalSection1(&m_CriticalSection);
	WORD	status(NO_ERRORS);
	
	strcpy(m_nDataOutBuf, "EC00xxx");

	i2b(m_nErrorCnt, 4);
	theApp.LeaveCriticalSection1(&m_CriticalSection);

	return status;
}
